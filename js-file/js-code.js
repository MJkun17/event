$(document).ready(function () {
    function playFile() {
        $(".player").not(this).each(function () {
            $(this).get(0).pause();
        });
        this[this.get(0).paused ? "play" : "pause"]();
    }

    $('.player').on("click play", function() {
        playFile.call(this);
    });
})
